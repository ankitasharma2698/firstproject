    <p>Coronary artery disease, also known as ischemic heart disease, is caused by atherosclerosis:a build-up of fatty material along the inner walls of the arteries. These fatty deposits known as atherosclerotic plaques narrow the coronary arteries, and if severe may reduce blood flow to the heart.If a narrowing (or stenosis) is relatively minor then the patient may not experience any symptoms. Severe narrowings may cause chest pain (angina) or breathlessness during exercise or even at rest. The thin covering of an atherosclerotic plaque can rupture, exposing the fatty centre to the circulating blood. In this case a clot or thrombus can form, blocking the artery, and restricting blood flow to an area of heart muscle causing a myocardial infarction (a heart attack) or unstable angina.In the worst case this may cause cardiac arrest, a sudden and utter loss of output from the heart.Obesity, high blood pressure, uncontrolled diabetes, smoking and high cholesterol can all increase the risk of developing atherosclerosis and coronary artery disease.</p>
