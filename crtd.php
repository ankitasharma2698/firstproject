    <p><p>Sometimes the heart pumps poorly. In other words, the heart beats out of sync. This can be caused by damage to the heart, age, genetics, certain medicines, and other reasons. CRT helps the heart pump normally by helping the right and left ventricles of the heart pump together. To do this, special wires are placed in the heart to pace the heart muscle in a specific way that improves the pumping function.A CRT-D is a small device that combines cardiac resynchronization therapy with defibrillation. It is placed under the skin of the chest. Wires (called “leads”) connect the CRT-D to the heart. A CRT-D is designed to prevent an at-risk person from dying suddenly from a dangerous heart rhythm. CRT-Ds sense dangerous rhythms and treat them right away. The CRT-D uses pacing or an electrical shock to stop a dangerous heart rhythm and change it to a normal heart rhythm. This happens much faster than a person could get to the hospital for treatment.</p>
